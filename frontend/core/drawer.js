const API = 'http://localhost:5000';

export function init(openCamera, navigate) {
  const hamburgerBtn = document.getElementById('hamburgerBtn');
  const drawer = document.getElementById('drawer');
  const drawerOverlay = document.getElementById('drawerOverlay');
  const drawerClose = document.getElementById('drawerClose');

  function open() {
    drawer.classList.add('open');
    drawerOverlay.classList.add('open');
  }
  function close() {
    drawer.classList.remove('open');
    drawerOverlay.classList.remove('open');
  }

  hamburgerBtn.addEventListener('click', open);
  drawerOverlay.addEventListener('click', close);
  drawerClose.addEventListener('click', close);

  drawer.querySelectorAll('[data-nav]').forEach(item => {
    item.addEventListener('click', async () => {
      const nav = item.dataset.nav;

      close();

      if (nav === 'scan') {
        setTimeout(() => openCamera(), 300);
      } else if (nav === 'export') {
        try {
          const res = await fetch(`${API}/member`);
          const data = await res.json();
          window.location.href = `${API}/export/${data.member_id}`;
        } catch {
          alert('Could not reach the server. Is it running?');
        }
      } else if (nav === 'dashboard') {
        navigate('home');
      } else if (nav === 'items') {
        navigate('items');
      } else if (nav === 'resources') {
        navigate('resources');
      }
    });
  });
}
